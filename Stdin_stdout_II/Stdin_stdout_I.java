package Day_3;

import java.util.*;

public class Stdin_stdout_II {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int a = scan.nextInt();
        int b = scan.nextInt();
        int c = scan.nextInt();
       
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        scan.close();
    }
    
}

// Input Sample 1:
// 1 2 3
// Input Sample 2: 
// 1
// 2
// 3
// Output is same for both:
// 1
// 2
// 3


